    public void LoadDataToJTable() {
        model.setRowCount(0);
        try {
            Connection con = DriverManager.getConnection(url, user, pass);
            Statement st = con.createStatement();
            String sql = "select * from NhanVien";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                String manv = rs.getString(1);
                String makhau = rs.getString(2);
                String hoten = rs.getString(3);
                Boolean gioitinh = rs.getBoolean(4);
                String quequan = rs.getString(5);
                Float sdt = rs.getFloat(6);
                String email = rs.getString(7);
                Date ngaysinh = rs.getDate(8);
                String chucvu = rs.getString(9);
                String bacluong = rs.getString(10);
                String mapb = rs.getString(11);
                NhanVien nv = new NhanVien(manv, makhau, hoten, gioitinh, quequan, sdt, email, ngaysinh, chucvu, bacluong, mapb);
                list.add(nv);
                Object[] row = new Object[]{manv, makhau, hoten, gioitinh, quequan, sdt, email, ngaysinh, chucvu, bacluong, mapb};
                model.addRow(row);
            }
            tblList.setModel(model);
            con.close();
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

